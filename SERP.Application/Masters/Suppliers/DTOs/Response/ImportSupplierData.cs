namespace SERP.Application.Masters.Suppliers.DTOs.Response
{
    public class ImportSupplierData
    {
        public int index { get; set; }

        /// <summary>
        /// Supplier No.
        /// </summary>
        public string? supplier_no { get; set; }

        /// <summary>
        /// Supplier Name.
        /// </summary>
        public string? supplier_name { get; set; }

        /// <summary>
        /// Status Flag (E: Enabled, D: Disabled).
        /// </summary>
        public string? status_flag { get; set; }

        /// <summary>
        /// PO Sending Method (e.g., PDF, EDI).
        /// </summary>
        public string? po_sending_method { get; set; }

        /// <summary>
        /// Authorised Flag (1: YES, 0: NO).
        /// </summary>
        public bool? authorised_flag { get; set; }

        /// <summary>
        /// Payment Term (e.g., 60, 30, ADV).
        /// </summary>
        public string? payment_term { get; set; }

        /// <summary>
        /// Payment Method
        /// </summary>
        public string? payment_method { get; set; }

        /// <summary>
        /// Default Currency (e.g., CNY, USD, SGD).
        /// </summary>
        public string? default_currency { get; set; }

        /// <summary>
        /// Cost Rule.
        /// </summary>
        public string? cost_rule { get; set; }

        /// <summary>
        /// Inco Term (e.g., FOB, CIF, DAP).
        /// </summary>
        public string? inco_term { get; set; }

        /// <summary>
        /// Product Flag (1: YES, 0: NO).
        /// </summary>
        public bool? product_flag { get; set; }

        /// <summary>
        /// Intermediary Supplier No.
        /// </summary>
        public string? intermediary_supplier_no { get; set; }

        /// <summary>
        /// Default Freight Method (e.g., SEA, ROAD-TRUCK).
        /// </summary>
        public string? default_freight_method { get; set; }

        /// <summary>
        /// Default Country of Loading
        /// </summary>
        public string? default_country_of_loading { get; set; }

        /// <summary>
        /// Default Port of Loading
        /// </summary>
        public string? default_port_of_loading { get; set; }

        /// <summary>
        /// Default Port of Discharge
        /// </summary>
        public string? default_port_of_discharge { get; set; }

        /// <summary>
        /// Default Country of Discharge
        /// </summary>
        public string? default_country_of_discharge { get; set; }

        /// <summary>
        /// Site No.
        /// </summary>
        public string? site_no { get; set; }
    }
}
